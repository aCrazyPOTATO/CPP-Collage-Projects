#include <iostream>
using namespace std;

int main()
{
    /*For Loop */

    for (int i = 0; i <= 100; i++)
    {
        cout << i << endl;
    }

    /*While*/
    int i = 0;
    while (i < 100)
    {
        cout << i << endl;
        i++;
    }

    /*Do Loop*/
    int i = 0;
    do
    {
        cout << i << endl;
        i++;
    } while (i < 100);
}